package com.example.tugas4;

public class Listmenuview {
    private String j,tb;
    private int gb;

    public Listmenuview(String j, String tb, int gb) {
        this.j = j;
        this.tb = tb;
        this.gb = gb;
    }

    public String getJ() {
        return j;
    }

    public void setJ(String j) {
        this.j = j;
    }

    public String getTb() {
        return tb;
    }

    public void setTb(String tb) {
        this.tb = tb;
    }

    public int getGb() {
        return gb;
    }

    public void setGb(int gb) {
        this.gb = gb;
    }
}
