package com.example.mykedua;

public class Perhitungan {
    public int tambah(int a, int b) {
        int c= a+b;
        return c;
    }
    public int kurang(int a, int b) {
        int c= a-b;
        return c;
    }
    public double kali(double a, double b) {
        double c= a*b;
        return c;
    }
    public double bagi(double a, double b) {
        double c= a/b;
        return c;
    }
}
